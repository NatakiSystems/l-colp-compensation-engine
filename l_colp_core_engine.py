# CORE CALCULATION LAYER
# ---------------------------------------------------------------------
def calculate_l_colp(col_index, wage_index, inflation_rate, alt_data_score, w1=0.40, w2=0.25, w3=0.15, w4=0.20):
    """Calculates the weighted premium percentage index based on architectural weights."""
    l_colp_index = (w1 * col_index) + (w2 * wage_index) + (w3 * inflation_rate) + (w4 * alt_data_score)
    return round(l_colp_index, 2)

def calculate_localized_salary(base_salary, l_colp_score):
    """Translates the L-COLP index into an explicit cash premium and final salary package."""
    premium_dollar_amount = base_salary * l_colp_score
    final_total_salary = base_salary + premium_dollar_amount
    return premium_dollar_amount, final_total_salary

